#Leia dois números e imprima se o primeiro é maior que o segundo.


num1 = float(input("Digite o um número: "))

num2 = float(input("Digite o outro numero número: "))


if num1 > num2:
    print("O primeiro número é maior que o segundo.")
elif num1 < num2:
    print("O primeiro número é menor que o segundo.")
else:
    print("Os dois números são iguais.")
